Felix Wilfred was born in Tamil Nadu, India in 1948. He is the President of the faculty of arts, and Chairman of The School of Philosophy and Religious thought, State University of Madras. He is also a member of the Statutory Ethical Committee of Indian Institute of Technology, Madras. He was a member of International Theological Commission of the Vatican. As visiting professor, he has taught at the Universities of Nijmegen, Munster, Frankfurt am Main, Boston College and Ateneo de Manila. His researches and field studies today cut across many disciplines in humanities and social sciences. His more recent publications in the field of Theology are "On the Banks of Ganges" (2002), "Asian Dreams and Christian Hope" (2003), "The Sling of Utopia: Struggles for a Different Society" (2005), and "Margins: Site of Asian Theologies (2008).
